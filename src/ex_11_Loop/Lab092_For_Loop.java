package ex_11_Loop;

public class Lab092_For_Loop {
    public static void main(String[] args) {
        // For Loop
        // Initialization -> Condition -> Updation(Increment/Decrement)
        // ICU

        for(int i=0;i < 10;i++){
            System.out.println(i); // it will print 0-9 &   it has 10 time run.
        }
    }
}
