package ex_11_Loop;

public class Lab097_For_If_Loop {
    public static void main(String[] args) {
        for(int shruti = 0 ; shruti < 18; shruti++){
            System.out.println(shruti);
            if (shruti >15){
                System.out.println("Gift From Papa");
            }
            else {
                System.out.println("She will get only Hugs and Candy!!");
            }
        }
    }
}
