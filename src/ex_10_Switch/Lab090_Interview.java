package ex_10_Switch;

import java.util.Scanner;

public class Lab090_Interview {
    public static void main(String[] args) {

        char code = 'C';

        switch (code){
            default :
                System.out.println("Hellooooooo");
                break;
            case 'A' :
                System.out.println("65");
                break;
            case 'B' :
                System.out.println("66");
                break;



        }
    }
}
